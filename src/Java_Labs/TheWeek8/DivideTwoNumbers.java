public class DivideTwoNumbers {
    public static void main(String[] args) {
        for (int i = 1; 1 <= 5; i++) {
            System.out.println("Lap " + i + " is finished.");

            for (int j = 1; j <= 3; j++) {
                System.out.println("..................Push-ups" + j + " is done.");
            }
        }
    }
}
