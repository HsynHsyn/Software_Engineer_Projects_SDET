package Muhtar_SDET.Day04_Primitive_Castings_Concatenation;

public class Concatenation_vs_Addition {
    public static void main(String[] args) {

        System.out.println("32"+1); //"321"
        System.out.println(7+"1");
        System.out.println("13"+"23"); //"1223"

        System.out.println("The value is "+true); // The value is true
    }
}
