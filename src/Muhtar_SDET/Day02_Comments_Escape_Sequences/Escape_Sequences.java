package Muhtar_SDET.Day02_Comments_Escape_Sequences;

public class Escape_Sequences {
    public static void main(String[] args) {
        System.out.println("Hello students\nToday we have java class"); // \n new line
        System.out.println("=====================================");
        System.out.println("\tJava programming Language"); //               \t paragraph
        System.out.println("\t\tJava programming Language"); //              \t paragraph space
        System.out.println("=====================================");
        System.out.println("\\");                                           // print \\ backslash
        System.out.println("=====================================");
        System.out.println("My favorite TV shows is \"After Life\"");       // printing \" double quote
        System.out.println("Hakan will go to \" school \" today ");
    }
}
