package Day5_UnaryAssignment_Relational_LogicalOperators;

public class example_increment_decrement_operators {

	public static void main(String[] args) {
		
		
		int b = 32;
		
		System.out.println(b++); 
		System.out.println(b); 

		System.out.println(b--);
		System.out.println(b);
		
		System.out.println("***************************");
		System.out.println(++b); 
		System.out.println(b); 

		System.out.println(--b); 
		System.out.println(b); 

	}

}
