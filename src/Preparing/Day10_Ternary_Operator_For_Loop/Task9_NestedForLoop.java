
package Preparing.Day10_Ternary_Operator_For_Loop;

public class Task9_NestedForLoop {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i += 1) {

            for (int j = 1; j <= i; j += 1) {

                System.out.print(j + " ");

            }
            System.out.println();

        }

    }

}
